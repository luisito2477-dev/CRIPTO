    private int obtenerNValue(){
        try {
            int n = Integer.parseInt(nValueField.getText().trim());
            if (n <= 0) {
                throw new IllegalArgumentException("n debe ser mayor a 0");
            }
            return n;
        } catch (NumberFormatException e) {
            throw new IllegalArgumentException("n debe ser un entero valido");
        }
    }
    
    
    
