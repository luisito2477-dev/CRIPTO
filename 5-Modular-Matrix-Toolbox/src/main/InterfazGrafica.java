    //Funcion para copiar la matriz en los campos
    private void setMatrizEnCampos(int[][] matriz, boolean esMatrizA) {
        if (matriz == null) return;

        javax.swing.JTextField[][] campos;

        if (esMatrizA) {
            campos = new javax.swing.JTextField[][] {
                {mA_0_0_TextField, mA_0_1_TextField, mA_0_2_TextField},
                {mA_1_0_TextField, mA_1_1_TextField, mA_1_2_TextField},
                {mA_2_0_TextField, mA_2_1_TextField, mA_2_2_TextField}
            };
        } else {
            campos = new javax.swing.JTextField[][] {
                {mB_0_0_TextField, mB_0_1_TextField, mB_0_2_TextField},
                {mB_1_0_TextField, mB_1_1_TextField, mB_1_2_TextField},
                {mB_2_0_TextField, mB_2_1_TextField, mB_2_2_TextField}
            };
        }

        int tam = matriz.length;

        for (int i = 0; i < tam; i++) {
            for (int j = 0; j < tam; j++) {
                campos[i][j].setText(String.valueOf(matriz[i][j]));
            }
        }
    }
    
    
    private void mostrarResultadoConOpciones(int[][] resultado, String mensaje) {
    String strMatriz = MatrixOperations.matrizAString(resultado);

    Object[] opciones = {"Guardar en A", "Guardar en B", "Cancelar"};

    int seleccion = JOptionPane.showOptionDialog(
        this,
        mensaje + "\n" + strMatriz,
        "Resultado",
        JOptionPane.DEFAULT_OPTION,
        JOptionPane.INFORMATION_MESSAGE,
        null,
        opciones,
        opciones[0]
    );

    if (seleccion == 0) {
        setMatrizEnCampos(resultado, true);
    } else if (seleccion == 1) {
        setMatrizEnCampos(resultado, false);
    }
}
    
    
